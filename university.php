    <h1>My University Experience so far</h1>
    <p>As I reflect on my experience in university, I am filled with mixed emotions. It has been a journey that has
        taught me a lot about myself and the world around me. University hasn't been so hard, but it has been complex
        and taken a very long learning curve. I remember walking into my first lecture hall and feeling like I was in a
        completely different world. Some courses were like the ones I learned in secondary school, while some others
        were far from it. Nonetheless, I was determined to make the most of my time in university.</p>
    <p>One of the things I did was to make friends in various departments. I figured that you never know when you're
        going to need someone. I found myself attending classes in different faculties, and I met people who were
        studying different things from me. It was refreshing to learn about different fields of study and how they
        intersected with my own.</p>
    <p>My departmental courses have been okay as well. Assignments and tests come and go, and sleepless nights are a
        part of the experience, but I'm still standing. I try my best to keep up with the coursework and ask for help
        when I need it. It's a good thing that I have friends who are also studying computer science. We help each other
        out and make the journey less daunting.</p>
    <p>Living alone has been an adventure on its own. I stay alone in my room, with no roommate. I like the
        independence, but it can get lonely at times. I don't dwell on it too much, though. I cook and think I am a very
        good cook. I have no choice but to eat my own food. Cooking has become a therapeutic activity for me, and I love
        trying out new food.</p>
    <p>When I feel overwhelmed with schoolwork, I go for walks to cool my head. It's a great way to clear my mind and
        get some fresh air. I try to spend as little time in my room as possible because it can get depressing after a
        while. Did I mention I am studying computer science? My bad, that takes a lot of work and dedication. I've had
        to sacrifice some things to keep up with the coursework, but it's worth it.</p>
    <p>Exams can be nerve-wracking, but I try my best and hope for the best. However, I also enjoy my school and the
        challenges that come with it. I've learned a lot about time management, resilience, and hard work. These are
        skills that I know will serve me well beyond my university days.</p>
    <p>As I look back on my time in university so far, I am grateful for the lessons I've learned and the people I've
        met. It hasn't been easy, but it has been worth it. I am excited to see what the future holds and how my
        experiences in university will shape me into the person I am meant to be.</p>
